package main

import "fmt"

func main() {
	// string menampilkan teks
	fmt.Println("Ilham")
	fmt.Println("Ilham Muhammad")
	fmt.Println("Ilham Muhammad Haikal")

	// sting menghitung jumlah karakter dan mengambil karakter yang ditenetukan
	fmt.Println(len("Ilham"))
	fmt.Println("Ilham Muhammad"[0])
	fmt.Println("Ilham Muhammad Haikal"[1])
}
